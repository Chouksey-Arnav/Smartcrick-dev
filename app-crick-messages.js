// Deterministic daily message picker — seeded by date + userId so it's
// the same for a user all day, but different across days/users, and
// doesn't repeat within 30 days (tracked in localStorage).
function hashStr(s) {
  var h = 0;
  for (var i = 0; i < s.length; i++) { h = ((h << 5) - h + s.charCodeAt(i)) | 0; }
  return Math.abs(h);
}

function mulberry32(seed) {
  return function() {
    seed |= 0; seed = (seed + 0x6D2B79F5) | 0;
    var t = Math.imul(seed ^ (seed >>> 15), 1 | seed);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

var HISTORY_KEY = 'crick_msg_history';
var HISTORY_DAYS = 30;

function readHistory() {
  try {
    if (A.DB && A.DB.get) {
      return A.DB.get(HISTORY_KEY) || {};
    }
    var raw = window.localStorage.getItem(HISTORY_KEY);
    return raw ? JSON.parse(raw) : {};
  } catch (e) { return {}; }
}

function writeHistory(hist) {
  try {
    if (A.DB && A.DB.set) { A.DB.set(HISTORY_KEY, hist); return; }
    window.localStorage.setItem(HISTORY_KEY, JSON.stringify(hist));
  } catch (e) {}
}

function pruneHistory(hist, todayStr) {
  var today = new Date(todayStr);
  var cutoff = new Date(today.getTime() - HISTORY_DAYS * 24 * 60 * 60 * 1000);
  var pruned = {};
  Object.keys(hist).forEach(function(key) {
    var d = new Date(hist[key]);
    if (!isNaN(d.getTime()) && d >= cutoff) { pruned[key] = hist[key]; }
  });
  return pruned;
}

function todayStr() {
  var d = new Date();
  var mm = String(d.getMonth() + 1).padStart ? String(d.getMonth() + 1).padStart(2, '0') : ('0' + (d.getMonth()+1)).slice(-2);
  var dd = String(d.getDate()).padStart ? String(d.getDate()).padStart(2, '0') : ('0' + d.getDate()).slice(-2);
  return d.getFullYear() + '-' + mm + '-' + dd;
}

A.getCrickDailyMessages = function(count, categories) {
  count = Math.max(1, Math.min(5, count || 1));
  var allCats = Object.keys(CRICK_MESSAGES_DB);
  var cats = (categories && categories.length) ? categories.filter(function(c){ return allCats.indexOf(c) !== -1; }) : allCats;
  if (!cats.length) cats = allCats;

  var userId = (A.DB && A.DB.getUser && (A.DB.getUser() || {}).id) || 'anon';
  var dateStr = todayStr();
  var seed = hashStr(dateStr + '::' + userId);
  var rnd = mulberry32(seed);

  var hist = pruneHistory(readHistory(), dateStr);

  // Build full candidate pool of {category, index, text}
  var pool = [];
  cats.forEach(function(cat) {
    var arr = CRICK_MESSAGES_DB[cat] || [];
    for (var idx = 0; idx < arr.length; idx++) {
      var key = cat + '::' + idx;
      if (!hist[key]) { pool.push({ category: cat, index: idx, text: arr[idx] }); }
    }
  });

  // If pool too small (e.g. heavy history), fall back to full set ignoring history
  if (pool.length < count) {
    pool = [];
    cats.forEach(function(cat) {
      var arr = CRICK_MESSAGES_DB[cat] || [];
      for (var idx = 0; idx < arr.length; idx++) {
        pool.push({ category: cat, index: idx, text: arr[idx] });
      }
    });
  }

  // Deterministic shuffle (Fisher-Yates) using seeded rnd, then take `count`
  for (var i = pool.length - 1; i > 0; i--) {
    var j = Math.floor(rnd() * (i + 1));
    var tmp = pool[i]; pool[i] = pool[j]; pool[j] = tmp;
  }

  var picked = pool.slice(0, count);

  picked.forEach(function(p) {
    var key = p.category + '::' + p.index;
    hist[key] = dateStr;
  });
  writeHistory(hist);

  var streakNum = 0;
  try {
    if (A.DB && A.DB.getProgress) { streakNum = (A.DB.getProgress() || {}).streak || 0; }
  } catch (e) {}

  return picked.map(function(p) {
    var text = p.text.replace(/\{streak\}/g, String(streakNum));
    return { text: text, category: p.category };
  });
};

